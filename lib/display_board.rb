# Define display_board that accepts a board and prints
# out the current state.
def display_board(arry)
  print " #{arry[0]} | #{arry[1]} | #{arry[2]} "
  print "-----------"
  print " #{arry[3]} | #{arry[4]} | #{arry[5]} "
  print "-----------"
  print " #{arry[6]} | #{arry[7]} | #{arry[8]} "
end

